package com.worksystem.exception;

/**
 * @author wsw16
 */
public class StudentRuntimeException extends Exception {

	public StudentRuntimeException() {
		super();
	}

	public StudentRuntimeException(String message, Throwable cause) {
		super(message, cause);
	}

	public StudentRuntimeException(String message) {
		super(message);
	}

	public StudentRuntimeException(Throwable cause) {
		super(cause);
	}


}
